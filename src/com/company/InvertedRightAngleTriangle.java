package com.company;

public class InvertedRightAngleTriangle {
    public static void main(String[] args) {
        int n=4;
        int i=1;
        for (n=i;n<=i;i++){
            for (int j=1;j<=i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
